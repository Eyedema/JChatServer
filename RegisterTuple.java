package unifi.inf.rc.UbaldoPuocci;

import java.net.InetAddress;

public class RegisterTuple {
	
	private InetAddress host;
	private int port;
	
	public RegisterTuple(InetAddress host, int port) {
		this.host = host;
		this.port = port;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((host == null) ? 0 : host.hashCode());
		result = prime * result + port;
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		RegisterTuple other = (RegisterTuple) obj;
		if (host == null) {
			if (other.host != null)
				return false;
		} else if (!host.equals(other.host))
			return false;
		if (port != other.port)
			return false;
		return true;
	}

	public InetAddress getHost() {
		return host;
	}

	public void InetAddress(InetAddress host) {
		this.host = host;
	}

	public int getPort() {
		return port;
	}

	public void setPort(int port) {
		this.port = port;
	}

}
